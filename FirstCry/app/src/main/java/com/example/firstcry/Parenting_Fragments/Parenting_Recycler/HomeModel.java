package com.example.firstcry.Parenting_Fragments.Parenting_Recycler;

public class HomeModel {

    int image;
    String title;

    public HomeModel(int image, String title) {
        this.image = image;
        this.title = title;
    }

    public int getImage() {
        return image;
    }

    public String getTitle() {
        return title;
    }
}
